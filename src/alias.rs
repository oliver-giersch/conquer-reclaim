use crate::record::Record;
use crate::traits::ReclaimBase;

////////////////////////////////////////////////////////////////////////////////////////////////////
// AssocRecord (alias)
////////////////////////////////////////////////////////////////////////////////////////////////////

pub(crate) type AssocRecord<T, R> = Record<<R as ReclaimBase>::Header, T>;

////////////////////////////////////////////////////////////////////////////////////////////////////
// RetiredRecord (alias)
////////////////////////////////////////////////////////////////////////////////////////////////////

pub(crate) type RetiredRecord<R> = Record<<R as ReclaimBase>::Header, <R as ReclaimBase>::Retired>;
